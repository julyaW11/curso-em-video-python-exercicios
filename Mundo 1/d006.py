numero=float(input('Digite um número:'))
dobro=numero*2
triplo=numero*3
rq=numero**(1/2)
print('O dobro do numero {} é {} seu triplo é {} e sua raiz quadrada é {}\n'.format(numero,dobro,triplo,rq) )

